package com.bitwise.jsploginapp;
 
public class Ocupational {
	private String occupation;
	private String presentC;
	private String lastC;
	private String salary;
	private String totalExp;

	public void setOccupation(String occupation) {
		this.occupation = occupation;
	}

	public String getOccupation() {
		return occupation;

	}

	public void setPresentC(String presentC) {
		this.presentC = presentC;
	}

	public String getPresentC() {
		return presentC;

	}

	public void setLastC(String lastC) {
		this.lastC = lastC;
	}

	public String getLastC() {
		return lastC;

	}

	public void setSalary(String salary) {
		this.salary = salary;
	}

	public String getSalary() {
		return salary;

	}

	public void setTotalExp(String totalExp) {
		this.totalExp = totalExp;
	}

	public String getTotalExp() {
		return totalExp;

	}

}
